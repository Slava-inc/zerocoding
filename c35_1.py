# Простой калькулятор

# Ввод двух чисел пользователем
num1 = float(input("Введите первое число: "))
num2 = float(input("Введите второе число: "))

# Выполнение всех элементарных математических операций
print("\nРезультаты действий:")
print(f"{num1} + {num2} = {num1 + num2}")      # Сложение
print(f"{num1} - {num2} = {num1 - num2}")      # Вычитание
print(f"{num1} * {num2} = {num1 * num2}")      # Умножение
    
# Деление с проверкой на деление на ноль
if num2 != 0:
    print(f"{num1} / {num2} = {num1 / num2}")  # Деление
    print(f"{num1} // {num2} = {num1 // num2}")# Целочисленное деление
    print(f"{num1} % {num2} = {num1 % num2}")  # Остаток от деления
else:
    print(f"{num1} / {num2} — деление невозможно (на ноль делить нельзя)")
    print(f"{num1} // {num2} — невозможно")
    print(f"{num1} % {num2} — невозможно")

print(f"{num1} ** {num2} = {num1 ** num2}")    # Возведение в степень
